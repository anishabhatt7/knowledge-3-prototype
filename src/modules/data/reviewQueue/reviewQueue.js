/** Look up a queue entry by id, returning a deep clone so callers
 *  can mutate (accept/reject) without polluting the seed. */
export function getQueueArticle(id) {
    const found = seedReviewQueue.find((a) => a.id === id);
    if (!found) return null;
    return JSON.parse(JSON.stringify(found));
}
